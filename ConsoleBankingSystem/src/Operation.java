// This enum represents the different types of operations that can be performed on a bank account.
public enum Operation {
    DEPOSIT,  // Represents a deposit operation
    WITHDRAW,  // Represents a withdrawal operation
    TRANSFER,  // Represents a money transfer operation
    CHECK_BALANCE,  // Represents a balance check operation
    LOGOUT,  // Represents a logout operation
    INVALID  // Represents an invalid operation
}
